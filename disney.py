disney_characters = ['simba', 'ariel', 'pumba', 'flounder', 'nala', 'ursula', 'scar', 'flotsam', 'timon']

for disney in disney_characters:
    if 'u' in disney:
        print(f"{disney}, U are so Uniquely U!")
    elif 'i' in disney:
        print(f"{disney}, I bet you're Impressively Intelligent!")
    elif 'o' in disney:
        print(f"{disney}, O My! How Original!")
    else:
        print(f"{disney}, Ehh, a's and e's are so ordinary.")
